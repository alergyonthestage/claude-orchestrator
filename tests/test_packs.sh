test_packs_md_removed_when_no_packs() {
    # If project has no packs section, a stale packs.md must be removed
    local tmpdir; tmpdir=$(mktemp -d); trap "rm -rf '$tmpdir'" EXIT
    setup_cco_env "$tmpdir"
    setup_global_from_defaults "$tmpdir"
    create_project "$tmpdir" "test-proj" "$(minimal_project_yml test-proj)"

    # Pre-create a stale packs.md (left from a previous run)
    printf '<!-- stale -->\n@/workspace/.packs/old/file.md\n' \
        > "$CCO_PROJECTS_DIR/test-proj/.claude/packs.md"

    run_cco start "test-proj" --dry-run

    # The stale file must be deleted
    if [[ -f "$CCO_PROJECTS_DIR/test-proj/.claude/packs.md" ]]; then
        echo "ASSERTION FAILED: stale packs.md should have been removed when project has no packs"
        return 1
    fi
}

