#!/usr/bin/env bash
# tests/test_packs.sh — knowledge pack generation tests
#
# Verifies that .claude/packs.md is correctly generated with instructional file list.
# Design Invariant 7: format must be - /workspace/.claude/packs/<name>/<file>

# Helper: create a standard single-pack project for testing (new schema)
_create_pack_project() {
    local tmpdir="$1"
    local pack_name="${2:-my-pack}"
    local pack_src="$tmpdir/pack-src-${pack_name}"
    mkdir -p "$pack_src"
    create_pack "$tmpdir" "$pack_name" "$(cat <<YAML
name: $pack_name
knowledge:
  source: $pack_src
  files:
    - overview.md
    - conventions.md
YAML
)"
    create_project "$tmpdir" "test-proj" "$(cat <<YAML
name: test-proj
auth:
  method: oauth
docker:
  ports: []
  env: {}
repos:
  - path: $CCO_DUMMY_REPO
    name: dummy-repo
packs:
  - $pack_name
YAML
)"
    echo "$pack_src"  # return pack source path for assertions
}

test_packs_md_file_is_created() {
    local tmpdir; tmpdir=$(mktemp -d); trap "rm -rf '$tmpdir'" EXIT
    setup_cco_env "$tmpdir"
    setup_global_from_defaults "$tmpdir"
    _create_pack_project "$tmpdir" > /dev/null
    run_cco start "test-proj" --dry-run
    assert_file_exists "$CCO_PROJECTS_DIR/test-proj/.claude/packs.md"
}

