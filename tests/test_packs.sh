test_no_knowledge_section_when_no_packs() {
    # If project has no packs, workspace.yml carries no knowledge section, and no
    # packs.md is ever produced. A stale committed packs.md is left untouched
    # (dry-run is side-effect free).
    local tmpdir; tmpdir=$(mktemp -d); trap "rm -rf '$tmpdir'" EXIT
    setup_cco_env "$tmpdir"
    setup_global_from_defaults "$tmpdir"
    create_project "$tmpdir" "test-proj" "$(minimal_project_yml test-proj)"

    # Pre-create a stale packs.md in the committed project tree (left from a
    # pre-R1 run). The committed <repo>/.cco/ is the :ro source (P5).
    local cco; cco=$(host_cco_dir "$tmpdir" "test-proj")
    printf '<!-- stale -->\n@/workspace/.claude/packs/old/file.md\n' \
        > "$cco/claude/packs.md"

    run_cco start "test-proj" --dry-run --dump

    # No packs.md is generated, ever
    assert_file_not_exists "$DRY_RUN_DIR/.claude/packs.md"
    # workspace.yml has no knowledge section when there are no packs
    if grep -q '^knowledge:' "$DRY_RUN_DIR/.claude/workspace.yml"; then
        echo "ASSERTION FAILED: workspace.yml should have no knowledge section without packs"
        return 1
    fi
    # Stale committed file is preserved (dry-run is side-effect free)
    assert_file_exists "$cco/claude/packs.md"
}

