#!/usr/bin/env bash
# tests/test_packs.sh — knowledge pack generation tests
#
# Since ADR-0041 R1 the knowledge index lives in the unified workspace.yml
# `knowledge:` section (no packs.md). Verifies that section is generated with
# the expected paths + descriptions.
# Design Invariant 7: knowledge path must be /workspace/.claude/packs/<name>/<file>

# Helper: create a standard single-pack project for testing (new schema)
_create_pack_project() {
    local tmpdir="$1"
    local pack_name="${2:-my-pack}"
    local pack_src="$tmpdir/pack-src-${pack_name}"
    mkdir -p "$pack_src"
    create_pack "$tmpdir" "$pack_name" "$(cat <<YAML
name: $pack_name
knowledge:
  source: $pack_src
  files:
    - overview.md
    - conventions.md
YAML
)"
    create_project "$tmpdir" "test-proj" "$(cat <<YAML
name: test-proj
auth:
  method: oauth
docker:
  ports: []
  env: {}
repos:
  - name: dummy-repo
packs:
  - $pack_name
YAML
)"
    echo "$pack_src"  # return pack source path for assertions
}

test_knowledge_section_generated() {
    # No packs.md is ever emitted; the knowledge index lives in workspace.yml
    local tmpdir; tmpdir=$(mktemp -d); trap "rm -rf '$tmpdir'" EXIT
    setup_cco_env "$tmpdir"
    setup_global_from_defaults "$tmpdir"
    _create_pack_project "$tmpdir" > /dev/null
    run_cco start "test-proj" --dry-run --dump
    assert_file_not_exists "$DRY_RUN_DIR/.claude/packs.md"
    assert_file_contains "$DRY_RUN_DIR/.claude/workspace.yml" "knowledge:"
}

