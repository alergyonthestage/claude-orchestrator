test_share_validate_warns_stale_pack() {
    local tmpdir; tmpdir=$(mktemp -d); trap "rm -rf '$tmpdir'" EXIT
    setup_cco_env "$tmpdir"
    setup_global_from_defaults "$tmpdir"
    run_cco pack create "will-delete"

    # Manually remove the pack dir but leave share.yml entry
    rm -rf "$CCO_PACKS_DIR/will-delete"

    # Validate should detect stale entry
    if run_cco share validate 2>/dev/null; then
        echo "ASSERTION FAILED: should have detected stale entry"
        return 1
    fi
}

