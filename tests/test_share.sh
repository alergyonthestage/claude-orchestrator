#!/usr/bin/env bash
# tests/test_share.sh — share.yml management tests
#
# Verifies share_init, share_refresh, share_validate, and the cco share
# CLI commands.

# ── share_init ────────────────────────────────────────────────────────

test_share_init_creates_share_yml() {
    local tmpdir; tmpdir=$(mktemp -d); trap "rm -rf '$tmpdir'" EXIT
    setup_cco_env "$tmpdir"
    setup_global_from_defaults "$tmpdir"
    run_cco init --lang "English"
    assert_file_exists "$CCO_USER_CONFIG_DIR/share.yml"
}

