}

test_share_refresh_removes_deleted_pack() {
    local tmpdir; tmpdir=$(mktemp -d); trap "rm -rf '$tmpdir'" EXIT
    setup_cco_env "$tmpdir"
    setup_global_from_defaults "$tmpdir"
    run_cco pack create "temp-pack"
    assert_file_contains "$CCO_USER_CONFIG_DIR/share.yml" "temp-pack"
    run_cco pack remove "temp-pack"
    assert_file_not_contains "$CCO_USER_CONFIG_DIR/share.yml" "temp-pack"
}

test_share_refresh_multiple_packs() {
    local tmpdir; tmpdir=$(mktemp -d); trap "rm -rf '$tmpdir'" EXIT
    setup_cco_env "$tmpdir"
    setup_global_from_defaults "$tmpdir"
    run_cco pack create "pack-a"
    run_cco pack create "pack-b"
    assert_file_contains "$CCO_USER_CONFIG_DIR/share.yml" "pack-a"
    assert_file_contains "$CCO_USER_CONFIG_DIR/share.yml" "pack-b"
}

test_share_refresh_preserves_name_and_description() {
    local tmpdir; tmpdir=$(mktemp -d); trap "rm -rf '$tmpdir'" EXIT
    setup_cco_env "$tmpdir"
    setup_global_from_defaults "$tmpdir"
    run_cco init --lang "English"

    # Set repo name and description
    cat > "$CCO_USER_CONFIG_DIR/share.yml" <<'YAML'
# Auto-generated by cco
name: "my-config"
description: "My personal config"

packs: []

templates: []
YAML

    # Create a pack (triggers refresh)
    run_cco pack create "new-pack"

    # Name and description should survive
    assert_file_contains "$CCO_USER_CONFIG_DIR/share.yml" "my-config"
    assert_file_contains "$CCO_USER_CONFIG_DIR/share.yml" "My personal config"
}

