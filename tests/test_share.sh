test_share_init_idempotent() {
    local tmpdir; tmpdir=$(mktemp -d); trap "rm -rf '$tmpdir'" EXIT
    setup_cco_env "$tmpdir"
    setup_global_from_defaults "$tmpdir"
    run_cco init --lang "English"

    # Modify share.yml
    printf '\n# USER_CANARY\n' >> "$CCO_USER_CONFIG_DIR/share.yml"

    # Second init should NOT overwrite
    run_cco init --lang "English"
    assert_file_contains "$CCO_USER_CONFIG_DIR/share.yml" "USER_CANARY"
}

# ── share_refresh ─────────────────────────────────────────────────────

